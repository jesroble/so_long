#include "ft_printf.h"

int	ft_pstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (*str != '\0')
	{
		write(1, &(*str), 1);
		str++;
		i++;
	}
	return (i);
}
